    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel)t1.getModel();
        try
        {
            Class.forName("java.sql.Driver");
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/project?zeroDateTimeBehavior=convertToNull", "root","root");
            Statement st=con.createStatement();
            String query="select * from signin;";
            ResultSet rs=st.executeQuery(query);
            while(rs.next())
            {
                String Name=rs.getString("Name");
                String Age=rs.getString("Age");
                String Gender=rs.getString("Gender");
                String PhoneNumber=rs.getString("PhoneNumber");
                String Address=rs.getString("Address");
                String City=rs.getString("City");
                String State=rs.getString("State");
                String Country=rs.getString("Country");
                String Pincode=rs.getString("Pincode");
                String Email=rs.getString("Email");
                model.addRow(new String[] {Name,Age,Gender,PhoneNumber,Address,City,State,Country,Pincode,Email});
            }
            rs.close();
            st.close();
            con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane,e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

